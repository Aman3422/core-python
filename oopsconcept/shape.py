class Shape:
    PI = 3.14

    def __init__(self,color,bw):
        self.__color=color
        self.__borderwidth=bw

    def setColor(self,c=""):
        self.__color = c
    def getColor(self):
        return self.__color
    def setBorderWidth(self,bw):
        self.__borderwidth = bw
    def getBorderWidth(self):
        return self.__borderwidth

    @staticmethod #DECORATOR
    def getPi():
        return Shape.PI




